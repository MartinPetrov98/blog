     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $ac = new Comment();
        $ac->user_id = $request->user_id;
        $ac->article_id = $request->article_id;
        // using htmlentitles to convert tags
        //then we can use   htmlspecialchars_decode() to display the styled comment

        $ac->comment = htmlspecialchars($request->{'article-ckeditor'});
        $ac->save();


        return Redirect::to('/');
    }

